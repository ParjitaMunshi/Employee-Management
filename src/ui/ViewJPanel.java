    private void btnviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tableEmployee.getSelectedRow();
       
        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
       DefaultTableModel model = (DefaultTableModel) tableEmployee.getModel();
       Employee selectedEmployee = (Employee) model.getValueAt(selectedRowIndex,0);
       
       TextName.setText(selectedEmployee.getName());
       TextEmployeeID.setText(String.valueOf(selectedEmployee.getEmployeeID()));
       TextAge.setText(String.valueOf(selectedEmployee.getAge()));
       TextGender.setText(selectedEmployee.getGender());
       TextStartDate.setText(String.valueOf(selectedEmployee.getStartDate()));
       TextLevel.setText(selectedEmployee.getLevel());
       TextTeamInfo.setText(selectedEmployee.getTeamInfo());
       TextPositionTitle.setText(selectedEmployee.getPositionTitle());
       TextCellPhoneNumber.setText(String.valueOf(selectedEmployee.getCellPhoneNumber()));
       TextEmailAddress.setText(selectedEmployee.getEmailAddress());
       
       ImageIcon ii = new ImageIcon(selectedEmployee.getProfilePath());
        Image image = ii.getImage().getScaledInstance(ProfilePic.getWidth(), ProfilePic.getHeight(), Image.SCALE_SMOOTH);
             
        ProfilePic.setIcon(new ImageIcon(image));
              
        
    }//GEN-LAST:event_btnviewActionPerformed

